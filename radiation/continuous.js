    constructor() {
        this.data = [];

        this.maxValue = 100;
    }

    add(x, y, value) {
        this.data.push(new RawRadiationNode(x, y, value));
    }

    get(x, y) {
        let num = 0, den = 0;
        const p = 2;

        for (const data of this.data) {
            const dx = data.x - x;
            const dy = data.y - y;
            const d2 = dx * dx + dy * dy;
            const w = d2 === 0 ? 1e10 : 1 / Math.pow(d2, p/2);

            num += w * data.value;
            den += w;
        }

        return num/den;
    }
